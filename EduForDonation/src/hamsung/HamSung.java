package hamsung;

public class HamSung {
	private int supportID;
	private String name;
	private String emailID;
	private String supportDate;
	private String department;
	private String size;
	private int contentAvailable;
	public int getSupportID() {
		return supportID;
	}
	public void setSupportID(int SupportID) {
		this.supportID = SupportID;
	}
	public String getName() {
		return name;
	}
	public void setName(String Name) {
		this.name = Name;
	}
	public String getEmailID() {
		return emailID;
	}
	public void setEmailID(String EmailID) {
		this.emailID = EmailID;
	}
	public String getSupportDate() {
		return supportDate;
	}
	public void setSupportDate(String SupportDate) {
		this.supportDate = SupportDate;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String Department) {
		this.department = Department;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String Size) {
		this.size = Size;
	}
	public int getContentAvailable() {
		return contentAvailable;
	}
	public void setContentAvailable(int ContentAvailable) {
		this.contentAvailable = ContentAvailable;
	}

	
}
