package main;

public class Main {
	private int supportID;
	private String name;
	private String emailID;
	private String supportDate;
	private String reasonForApplication;
	private String prosAndCons;
	private String programYouWant;
	private int contentAvailable;

	public int getSupportID() {
		return supportID;
	}

	public void setSupportID(int SupportID) {
		this.supportID = SupportID;
	}

	public String getName() {
		return name;
	}

	public void setName(String Name) {
		this.name = Name;
	}

	public String getEmailID() {
		return emailID;
	}

	public void setEmailID(String EmailID) {
		this.emailID = EmailID;
	}

	public String getSupportDate() {
		return supportDate;
	}

	public void setSupportDate(String SupportDate) {
		this.supportDate = SupportDate;
	}

	public String getReasonForApplication() {
		return reasonForApplication;
	}

	public void setReasonForApplication(String ReasonForApplication) {
		this.reasonForApplication = ReasonForApplication;
	}

	public String getProsAndCons() {
		return prosAndCons;
	}

	public void setProsAndCons(String ProsAndCons) {
		this.prosAndCons = ProsAndCons;
	}

	public String getProgramYouWant() {
		return programYouWant;
	}

	public void setProgramYouWant(String ProgramYouWant) {
		this.programYouWant = ProgramYouWant;
	}

	public int getContentAvailable() {
		return contentAvailable;
	}

	public void setContentAvailable(int ContentAvailable) {
		this.contentAvailable = ContentAvailable;
	}
}
